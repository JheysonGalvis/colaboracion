'''Métodos de Mayúsculas y Minúsculas:
Crea una cadena en minúsculas y conviértela a
mayúsculas usando los métodos upper() y lower().
'''
# Crear una cadena en minúsculas
texto = "hola mundo"

#convertir a mayusculas
mayusculas = texto.upper()
print("En mayusculas:", mayusculas)

#Convertir de nuevo a minúsculas
minusculas = mayusculas.lower()
print("En minusculas:", minusculas)
