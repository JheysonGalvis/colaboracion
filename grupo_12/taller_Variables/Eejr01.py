'''Operaciones con Cadenas:
Crea dos variables de cadena llamadas cadena1 y cadena2.
Concatena las dos cadenas y guarda el resultado en una nueva variable.
Imprime la longitud de la cadena resultante.
'''

cadena1 = "Hola" # Cadena 1 - 4 elementos
cadena2 = "Mundo" # Cadena 2 - 5 elementos
cadena3 = cadena1 + " " + cadena2
print(len(cadena3)) # Longitud de la cadena resultante - 10 elementos incluyendo el espacio entre las dos cadenas